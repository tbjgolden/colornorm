const nameRgbMap = new Map<string, [number, number, number, number]>(colorNames)
const rgbNameMap = new Map<string, string>(
  colorNames.map(([colorName, rgba]) => [rgba.join(','), colorName])
)

export const nameToRgb = (
  name: string
): [number, number, number, number] | null => {
  return nameRgbMap.get(name) ?? null
}

export const rgbToName = (
  rgba: [number, number, number, number]
): string | null => {
  return rgbNameMap.get(rgba.join(',')) ?? null
}
